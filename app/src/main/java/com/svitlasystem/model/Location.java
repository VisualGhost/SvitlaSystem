package com.svitlasystem.model;

public class Location {

    private int id;
    private String name;
    private double latitude;
    private double longitude;

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public double getLatitude() {
        return latitude;
    }

    public double getLongitude() {
        return longitude;
    }
}
